public class loops{
    public static void main(String[] args){
        for(int i=0;i<10;i++){
            System.out.println("vicky vicky");
        }
        int i = 0;
        while(i < 10){
            System.out.println("sneha sneha");
            i++;
        }
        do{
            System.out.println("hello");
            i++;
        }
        while(i < 10);
    }
}